use core::ffi::c_char;
use std::ffi::CStr;

// eraDtf2d  ―  Calendar + clock → 2-part Julian Date / quasi-JD
pub unsafe fn eraDtf2d(
    scale: *const c_char,
    iy: i32,
    im: i32,
    id: i32,
    ihr: i32,
    imn: i32,
    sec: f64,
    d1: *mut f64,
    d2: *mut f64,
) -> i32 {
    let mut dj = 0.0_f64;
    let mut w = 0.0_f64;
    let mut js = eraCal2jd(iy, im, id, &mut dj, &mut w);
    if js != 0 {
        return js;
    }
    dj += w;

    let mut day = ERFA_DAYSEC;
    let mut seclim = 60.0_f64;

    let scale_str = CStr::from_ptr(scale).to_bytes();
    if scale_str == b"UTC" {
        let mut dat0 = 0.0_f64;
        js = eraDat(iy, im, id, 0.0, &mut dat0);
        if js < 0 {
            return js;
        }
        let mut dat12 = 0.0_f64;
        js = eraDat(iy, im, id, 0.5, &mut dat12);
        if js < 0 {
            return js;
        }
        let mut iy2 = 0;
        let mut im2 = 0;
        let mut id2 = 0;
        js = eraJd2cal(dj, 1.5, &mut iy2, &mut im2, &mut id2, &mut w);
        if js != 0 {
            return js;
        }
        let mut dat24 = 0.0_f64;
        js = eraDat(iy2, im2, id2, 0.0, &mut dat24);
        if js < 0 {
            return js;
        }
        let dleap = dat24 - (2.0 * dat12 - dat0);

        day += dleap;
        if ihr == 23 && imn == 59 {
            seclim += dleap;
        }
    }

    if (0..=23).contains(&ihr) {
        if (0..=59).contains(&imn) {
            if sec >= 0.0 {
                if sec >= seclim {
                    js += 2;
                }
            } else {
                return -6;
            }
        } else {
            return -5;
        }
    } else {
        return -4;
    }

    let time = (60.0 * (60.0 * f64::from(ihr) + f64::from(imn)) + sec) / day;

    *d1 = dj;
    *d2 = time;

    js
}
